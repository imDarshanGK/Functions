def sum(n):
    s=0
    for i in range(1,n+1):
        s=s+i
    print('sum=',s)
    
def factorial(n):
    s=1
    for i in range(1,n+1):
        s=s*i
    print('factorial=',s)
    
def info(n,a,p):
    print("Name=",n,"Age=",a,"percentage=",p)
    
